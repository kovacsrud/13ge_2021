using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Ciklusok
{
    class Program
    {
        static void Main(string[] args)
        {
            //Ciklus, iteráció, ismételt tevékenység

            //előírt lépésszámú ciklus (for)

            for (int i = 0; i < 10; i++)
            {
                Console.WriteLine(i);
            }

            //elöltesztelő ciklus


            Console.ReadKey();
        }
    }
}
